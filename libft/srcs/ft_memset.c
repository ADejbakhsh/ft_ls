#include "libft.h"

void	*ft_memset(void *s, int c, size_t n)
{
	char*b;
	int a;

	a = 0;
	b = (char*)s;
	while (n > 0)
	{
		b[a] = c;
		a++;
		n--;
	}
	s = b;
	return ((void*)s);
}
