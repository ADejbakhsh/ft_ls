#include "libft.h"

void	ft_striteri(char *s, void (*f)(unsigned int, char *))
{
	size_t a;

	a = 0;
	if (s == NULL || f == NULL)
		return ;
	while (s[a])
	{
		(*f)(a, &s[a]);
		a++;
	}
}
